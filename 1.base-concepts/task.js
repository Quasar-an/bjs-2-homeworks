"use strict";
function solveEquation(a, b, c) {
  let discriminant = b ** 2 - 4 * a * c;
  let roots = [];
}

if (discriminant < 0) {
  return roots;
}

if (discriminant === 0) {
  let root = -b / (2 * a);
  roots.push(root);
  return roots;
}

if (discriminant === 0) {
  let root = -b / (2 * a);
  roots.push(root);
  return roots;
}